}

# Setup MCP integration
setup_mcp() {
    print_info "Setting up MCP integration..."
    
    # Check if MCP server can be started
    if [ -f "dist/orchestrator/mcp-server.js" ]; then
        print_success "MCP server ready"
        print_info "Start MCP server with: npm run mcp"
    else
        print_info "MCP server not built yet"
    fi
}

# Run tests
run_tests() {
    print_info "Running tests..."
    
    if [ -f "package.json" ] && grep -q "\"test\"" package.json; then
        npm test
        print_success "Tests passed"
    else
        print_info "No tests configured"
    fi
}

# Verify installation
verify_installation() {
    print_info "Verifying installation..."
    
    # Check if build output exists
    if [ -d "dist" ]; then
        print_success "Build directory found"
    else
        print_error "Build directory not found"
        return 1
    fi
    
    # Check if main file exists
    if [ -f "dist/index.js" ]; then
        print_success "Main file found"
    else
        print_error "Main file not found"
        return 1
    fi
    
    # Try to import the module
    node -e "require('./dist/index.js')" 2>/dev/null
    if [ $? -eq 0 ]; then
        print_success "Module loads successfully"
    else
        print_error "Module failed to load"
        return 1
    fi
    
    return 0
}

